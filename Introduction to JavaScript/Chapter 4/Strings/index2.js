// String Methods

// 1. length

let str1 = 'Prince';
console.log(str1.length);

// 2. ToUpperCase, ToLowerCase

console.log(str1.toUpperCase());
console.log(str1.toLowerCase());

// 3. Slice

console.log(str1.slice(2));
console.log(str1.slice(2, 4));

// 4. Replace

let str2 = 'Vanshi';
console.log(str2.replace('Vanshi', 'Vanshi Cutie'));

// 5. Concat

console.log(str1.concat(' Loves ', str2));

// 6. trim

let name = '    Prince  ';
console.log(name);
console.log(name.trim());
