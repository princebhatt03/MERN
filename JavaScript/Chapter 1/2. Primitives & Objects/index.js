// Primitive DataTypes...
// N, N, S, S, U, B, B

// 1. Null
let a = null;
console.log(a);
console.log(typeof a);

// 2. Number
let b = 123;
console.log(b);
console.log(typeof b);

// 3. String
let c = 'Prince';
console.log(c);
console.log(typeof c);

// 4. Symbol
let d = Symbol('I am a Symbol');
console.log(d);
console.log(typeof d);

// 5. Undefined
let e = undefined;
console.log(e);
console.log(typeof e);

// 6. Boolean
let f = false;
console.log(f);
console.log(typeof f);

// 7. BigInt
let g = BigInt('1603') + BigInt('3');
console.log(g);
console.log(typeof g);
