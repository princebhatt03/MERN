// Let, var, const
// // In JavaScript, there are three ways to declare variables: var, let, and const. Each has its own scope and behavior.
//
// 1. var: The var keyword is used to declare variables that are function-scoped or globally-scoped. It can be re-declared and updated within its scope. However, it has some quirks, such as hoisting, which can lead to unexpected behavior.
//
// 2. let: The let keyword is used to declare block-scoped variables. It can be updated but not re-declared within its scope. This makes it a better choice for most cases, as it avoids the issues associated with var.
//
// 3. const: The const keyword is used to declare block-scoped variables that cannot be updated or re-declared. It is used for constants or values that should not change throughout the program. However, it is important to note that const does not make the variable immutable; it only prevents re-assignment of the variable itself.
